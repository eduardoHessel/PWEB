var num1 = parseFloat(prompt("Insira o primeiro número."));
var num2 = parseFloat(prompt("Insira o segundo número."));

const soma = num1 + num2;
alert("A soma dos valores é " + soma);

const subtracao = num1 - num2;
alert("A subtração do primeiro pelo segundo é " + subtracao);

const produto = num1 * num2;
alert("O produto dos dois é " + produto);

const divisao = num1 / num2; 
alert("A divisão do primeiro pelo segundo é " + divisao);

const resto = num1 % num2;
alert("O resto da divisão do primeiro pelo segundo é " + resto);